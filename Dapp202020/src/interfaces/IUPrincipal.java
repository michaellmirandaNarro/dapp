    private void centrarVentana(JInternalFrame ventana){
        
        int x = (desktopPane.getWidth() / 2) - (ventana.getWidth() / 2);
        int y = (desktopPane.getHeight() / 2) - (ventana.getHeight() / 2);
        ventana.setLocation(x, y);
    }

