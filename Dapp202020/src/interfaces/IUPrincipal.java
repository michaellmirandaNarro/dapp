    private void manejadorClientes(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manejadorClientes
        if(evt.getSource()==mnuItemRegistroClientes){
            if(clientes!=null){
                desktopPane.remove(clientes);
            }
            clientes=new IFRClientes();
            desktopPane.add(clientes);
            centrarVentana(clientes);
            clientes.setVisible(true);
        }
    }//GEN-LAST:event_manejadorClientes

